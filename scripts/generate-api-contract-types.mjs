import { readdir, readFile, writeFile } from "node:fs/promises";

const sourceDirectory = new URL("../packages/api-contract/src/", import.meta.url);
const zodDirectory = new URL("zod/", sourceDirectory);
const sourceFiles = (await readdir(zodDirectory)).filter((file) =>
  file.endsWith(".ts")
);
const schemaSources = await Promise.all(
  sourceFiles.map((file) => readFile(new URL(file, zodDirectory), "utf8"))
);
const schemaNames = new Set(
  Array.from(
    schemaSources.join("\n").matchAll(/export const (\w+Schema)\b/g),
    (match) => match[1]
  )
);
const mappings = Array.from(schemaNames, (schemaName) => {
  const name = schemaName.slice(0, -"Schema".length);
  return {
    contractName: `${name[0].toUpperCase()}${name.slice(1)}`,
    schemaName
  };
}).sort(({ contractName: left }, { contractName: right }) =>
  left.localeCompare(right)
);
const schemaImports = mappings.map(({ schemaName }) => schemaName).sort();
const output = [
  "// Generated by scripts/generate-api-contract-types.mjs.",
  "// Do not edit directly; update the corresponding Zod schema instead.",
  'import type { z } from "zod";',
  "import {",
  ...schemaImports.map((schemaName) => `  ${schemaName},`),
  '} from "./schemas";',
  "",
  ...mappings.map(
    ({ contractName, schemaName }) =>
      `export type ${contractName} = z.infer<typeof ${schemaName}>;`
  ),
  ""
].join("\n");

await writeFile(new URL("contracts.ts", sourceDirectory), output);

const openApiAliases = [
  "// Generated by scripts/generate-api-contract-types.mjs.",
  "// Keeps existing OpenAPI component names while Zod remains authoritative.",
  "import {",
  ...schemaImports.map((schemaName) => `  ${schemaName},`),
  '} from "./schemas";',
  "",
  ...mappings.map(
    ({ contractName, schemaName }) =>
      `export const ${contractName} = ${schemaName};`
  ),
  ""
].join("\n");

await writeFile(
  new URL("zod/openapi-aliases.ts", sourceDirectory),
  openApiAliases
);
